package rqcode.stigs.win10_new.Categories;

public class RegEditTemplate {
    private String id;
    private String attr;
    private String path;
    private String path_short;
    private String result_value;
    private String description;

    public String getId() {
        return id;
    }

    public void setId(String id) {
        this.id = id;
    }

    public String getAttr() {
        return attr;
    }

    public void setAttr(String attr) {
        this.attr = attr;
    }

    public String getPath() {
        return path;
    }

    public void setPath(String path) {
        this.path = path;
    }

    public String getPath_short() {
        return path_short;
    }

    public void setPath_short(String path_short) {
        this.path_short = path_short;
    }

    public String getResult_value() {
        return result_value;
    }

    public void setResult_value(String result_value) {
        this.result_value = result_value;
    }

    public String getDescription() {
        return description;
    }

    public void setDescription(String description) {
        this.description = description;
    }
}
